namespace HmacManager.Components;

/// <summary>
/// An <c>enum</c> representing a <c>ContentHashAlgorithm</c>.
/// </summary>
public enum ContentHashAlgorithm
{
    /// <summary>
    /// Represents the SHA1 cryptographic hash function.
    /// </summary>
    SHA1,

    /// <summary>
    /// Represents the SHA256 cryptographic hash function.
    /// </summary>
    SHA256,

    /// <summary>
    /// Represents the SHA512 cryptographic hash function.
    /// </summary>
    SHA512
}